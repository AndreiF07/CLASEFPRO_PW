public class Operaciones {

    public void sumar(int op1, int op2) {
        int resultado = op1 + op2;
        System.out.println("Resultado: " + resultado);
    }

    public void restar(int op1, int op2) {
        int resultado = op1-op2;
        System.out.println("Resultado: " + resultado);
    }

}